#include "trap.h"    
    .org 0x0
    .text
    .global _start
_start:
    # =========================================================
    # 1. 基础 Forwarding 测试
    # =========================================================
    lu12i.w     $a0, 0x123
    addi.w      $a1, $a0, 0x456   
    # 预期：$a1 = 0x123456

    ori         $t0, $zero, 0xFF
    andi        $t1, $t0, 0xF0    
    # 预期：$t1 = 0xF0

    lu12i.w     $s0, 0x10         # s0 = 0x10000

    # =========================================================
    # 2. Store Forwarding 测试
    # =========================================================
    lu12i.w     $s1, 0x1
    ori         $s1, $s1, 0x234
    st.w        $s1, $s0, 0       
    
    lu12i.w     $s1, 0x5
    ori         $s1, $s1, 0x678
    st.w        $s1, $s0, 4
    
    lu12i.w     $s1, 0x9
    ori         $s1, $s1, 0xABC
    st.w        $s1, $s0, 8
    
    # =========================================================
    # 3. Load-Use Stall 测试
    # =========================================================
    ld.w        $t2, $s0, 0       
    addi.w      $t2, $t2, 1       
    # 预期：$t2 = 0x1235
    
    # =========================================================
    # 4. 【核心】分支刷新 (Flush) 测试
    # =========================================================
    ori         $t5, $zero, 0     # 初始化 $t5 = 0
    beq         $zero, $zero, flush_success # 必然跳转！
    
    # --- 危险区 (应该被 Flush) ---
    # 【修正】改用 ori，因为 0xBAD (2989) 适合无符号12位立即数
    ori         $t5, $zero, 0xBAD # 如果这条执行了，说明 Flush 失败！
    # ---------------------------
    
flush_success:
    # 如果代码跳到了这里，$t5 应该依然是 0
    
    # =========================================================
    # 5. 循环测试 (Forwarding + Flush 综合测试)
    # =========================================================
    addi.w      $t3, $zero, 5     # 循环 5 次

loop:
    addi.w      $t3, $t3, -1
    bne         $t3, $zero, loop
    
    # 循环结束后执行
    addi.w      $t4, $zero, 0x666 

    # 程序结束
    HIT_GOOD_TRAP